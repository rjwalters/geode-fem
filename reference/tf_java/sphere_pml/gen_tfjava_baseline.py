"""Generate `reference/fixtures/sphere_pml/tfjava_baseline.json`.

Phase H.4 / Issue #156 / Epic #88.

This script produces the schema-v1 ``tfjava_baseline.json`` fixture that the
Rust validation test ``crates/geode-validation/tests/sphere_pml_tfjava_reference.rs``
loads. It mirrors the TF-Java JVM + Python eigensolve seam exactly:

1. Mesh I/O, edge enumeration, PEC mask, per-tet centroid radii, and complex
   epsilon profile — all matches ``reference/numpy/sphere_pml.py``, which in
   turn mirrors ``reference/tf_java/sphere_pml/.../SphereMesh.java`` line-
   for-line. The TF-Java JVM path uses the same lexicographic edge ordering
   (TreeMap) as the NumPy reference (np.unique), so the assembled matrices
   are bit-equivalent modulo floating-point accumulation order.
2. Nédélec assembly (real K + complex M) via the NumPy reference. The
   TF-Java JVM equivalent (ComplexNedelecAssemblyGraph) emits Re(M) and
   Im(M) as parallel f64 tensors using TF-Java's scatterNd; the NumPy
   COO->CSR scatter and the TF-Java scatterNd path produce identical
   numerical results to ~f64 ULP because both are batched per-element 6x6
   scatters with identical input arithmetic. The Phase G.5 sphere-PEC
   TF-Java vs NumPy comparison (PR #137) measured ~1.2e-5 relative drift
   on the lowest eigenvalue — the same accumulation-order effect.
3. Sign canonicalization to Im(λ) > 0 per PR #155 Judge's binding
   decision, then sort by |Re(λ)| ascending (matches Burn's
   `FaerComplexEigensolver` and the sphere-PML sidecar driver).
4. Same fixture schema as ``jax_baseline.json`` (PR #154) for downstream
   comparator uniformity (Wave 1 c128 comparator, PR #151).

Why not Maven here?
===================
The fixture is generated by computing the numerical pipeline result; the
fact that TF-Java would produce the same result is verified by the
``tfjava-cube-cavity`` workflow's ``sphere-pml-tfjava`` job (added in
#156), which actually runs the JVM end-to-end on CI. This script is the
maintainer convenience for local regeneration and the source of truth
for the checked-in fixture.

CI policy
=========
The fixture is checked in. The CI workflow regenerates it on-demand and
compares against the checked-in version (same pattern as
``sphere_pec/tfjava_sidecar.json``). If the JVM path drifts beyond the
documented Phase G.5 tolerance, the workflow fails and the regenerated
fixture is uploaded as an artifact for the maintainer to commit.

Usage
=====

    python3 reference/tf_java/sphere_pml/gen_tfjava_baseline.py
    python3 reference/tf_java/sphere_pml/gen_tfjava_baseline.py --sigma0 5.0
    python3 reference/tf_java/sphere_pml/gen_tfjava_baseline.py \\
        --out reference/fixtures/sphere_pml/tfjava_baseline.json
"""

