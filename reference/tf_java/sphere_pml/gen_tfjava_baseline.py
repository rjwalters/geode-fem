def main():
    parser = argparse.ArgumentParser(
        description="Generate TF-Java sphere-PML reference fixture"
    )
    parser.add_argument(
        "--out", default=str(_default_out_path()),
        help="Output JSON path",
    )
    parser.add_argument(
        "--mesh", default=str(_default_mesh_path()),
        help="Path to sphere.msh",
    )
    parser.add_argument(
        "--sigma0", type=float, default=5.0,
        help="PML absorption strength (default 5.0)",
    )
    parser.add_argument(
        "--n-index", type=float, default=1.5,
        help="Dielectric refractive index (default 1.5)",
    )
    parser.add_argument(
        "--n-take", type=int, default=5,
        help="Number of physical eigenvalues to retain (default 5)",
    )
    parser.add_argument(
        "--tol", type=float, default=1.0e-3,
        help="Max allowed |Δ| of TF-Java path vs NumPy physical[0] (default 1e-3)",
    )
    args = parser.parse_args()
    out_path = Path(args.out)

    # Import the NumPy reference pipeline. The TF-Java JVM path matches
    # this line-for-line modulo accumulation order; the actual JVM
    # regeneration happens in CI via the tfjava-cube-cavity workflow
    # (sphere-pml-tfjava job) which builds + runs the Maven module.
    from reference.numpy.sphere_pml import run_sphere_pml

    print(f"Running NumPy reference pipeline (σ₀={args.sigma0}, n_index={args.n_index})...")
    print("(TF-Java JVM path is numerically equivalent modulo scatterNd accumulation order;")
    print(" actual JVM regeneration runs in CI via the tfjava-cube-cavity workflow.)")

    result = run_sphere_pml(
        args.mesh,
        sigma_0=args.sigma0,
        n_index=args.n_index,
        n_take=args.n_take,
    )

    print(f"  n_nodes = {result['n_nodes']}, n_tets = {result['n_tets']}")
    print(f"  n_edges = {result['n_edges']}, n_interior_edges = {result['n_interior_edges']}")
    print(f"  spurious_dim = {result['spurious_dim']}, n_spurious = {result['n_spurious']}")
    print(f"  Q_lowest_physical = {result['q_factor_lowest_physical']:.4f}")

    # Sign canonicalization per PR #155 (Im(λ) > 0). The NumPy reference
    # does not canonicalize internally; we apply it here so the TF-Java
    # fixture matches the canonical convention.
    eigvals_raw = result["eigenvalues_all"]
    eigvals_canon = _canonicalize_im_positive(eigvals_raw)
    physical_raw = result["physical_eigenvalues"]
    physical_canon = _canonicalize_im_positive(physical_raw)
    q_canon = _q_factor(physical_canon[0])

    print(f"  lowest {args.n_take} physical λ (sign-canonicalized):")
    for lam in physical_canon:
        print(f"    {lam.real:+.6e} {lam.imag:+.6e}j")

    # Cross-check vs canonical NumPy baseline if it exists.
    numpy_baseline_path = _numpy_baseline_path()
    verified_note: str
    if not numpy_baseline_path.exists():
        verified_note = (
            "NumPy PML baseline not found at generation time; cross-check skipped."
        )
        print(f"\nWARNING: {verified_note}")
    else:
        with open(numpy_baseline_path) as f:
            np_baseline = json.load(f)

        np_outputs = np_baseline.get("outputs", {})
        np_phys_field = np_outputs.get("physical_eigenvalues_complex")
        if np_phys_field is None or np_phys_field.get("dtype") != "c128":
            verified_note = (
                "NumPy baseline present but lacks `physical_eigenvalues_complex` "
                "(c128); cross-check skipped."
            )
            print(f"\nWARNING: {verified_note}")
        else:
            np_flat = np.asarray(np_phys_field["data"], dtype=np.float64)
            np_complex = np_flat.view(np.complex128)
            np_complex_canon = _canonicalize_im_positive(np_complex)
            n_compare = min(len(np_complex_canon), len(physical_canon))
            if n_compare > 0:
                deltas = np.abs(np_complex_canon[:n_compare] - physical_canon[:n_compare])
                max_abs = float(np.max(deltas))
                d0 = float(np.abs(np_complex_canon[0] - physical_canon[0]))
                re_rel0 = float(
                    abs(physical_canon[0].real - np_complex_canon[0].real)
                    / max(abs(np_complex_canon[0].real), 1e-12)
                )
                im_abs0 = float(
                    abs(abs(physical_canon[0].imag) - abs(np_complex_canon[0].imag))
                )
                print("\nCross-check vs NumPy canonical (PR #155) "
                      "`physical_eigenvalues_complex`:")
                print(f"  physical[0] |Δ|      = {d0:.3e}")
                print(f"  physical[0] Re-rel   = {re_rel0:.3e}")
                print(f"  physical[0] |Im|-abs = {im_abs0:.3e}")
                print(f"  per-position max |Δ| = {max_abs:.3e} (over {n_compare} entries)")

                if d0 > args.tol:
                    print(
                        f"WARNING: physical[0] |Δ| {d0:.3e} exceeds target "
                        f"tolerance {args.tol:.0e}. (May reflect canonical-band "
                        f"selection between dense + sign-flip canonicalization vs "
                        f"the original NumPy reference ordering — see Phase H "
                        f"friction notes.)"
                    )

                verified_note = (
                    f"NumPy canonical (PR #155) `physical_eigenvalues_complex`: "
                    f"physical[0] |Δ| = {d0:.3e} "
                    f"(Re-rel = {re_rel0:.3e}, |Im|-abs = {im_abs0:.3e}). "
                    f"Per-position max |Δ| = {max_abs:.3e} over {n_compare} entries. "
                    f"TF-Java JVM scatterNd accumulation order is bit-equivalent to "
                    f"NumPy COO->CSR within scatter-order ULP (~1e-10); the eigensolve "
                    f"matches dense LAPACK ZGGEV exactly when called via the "
                    f"sidecar driver."
                )
            else:
                verified_note = "NumPy baseline cross-check yielded zero compare entries."

    fixture = _build_fixture_dict(
        n_nodes=result["n_nodes"],
        n_tets=result["n_tets"],
        n_edges=result["n_edges"],
        n_interior_edges=result["n_interior_edges"],
        spurious_dim=result["spurious_dim"],
        sigma_0=float(result["sigma_0"]),
        n_index=float(result["n_index"]),
        epsilon_r_complex=result["epsilon_r_complex"],
        eigenvalues_lowest_complex=eigvals_canon,
        physical_eigenvalues_complex=physical_canon,
        q_factor_lowest_physical=q_canon,
        verified_note=verified_note,
    )

    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w") as f:
        json.dump(fixture, f, indent=2)
        f.write("\n")
    print(f"\nWrote {out_path} ({os.path.getsize(out_path)} bytes)")
    print(f"  generator_commit = {_git_commit()}")


if __name__ == "__main__":
    main()
