def _interleave_c128(z: np.ndarray) -> list[float]:
    """Real-imag interleaved encoding per `reference/SCHEMA.md` "Complex
    encoding (c128)"."""
    z = np.ascontiguousarray(z, dtype=np.complex128)
    return z.view(np.float64).tolist()


def _canonicalize_im_positive(eigvals: np.ndarray) -> np.ndarray:
    """Flip eigenvalues with Im(λ) < 0 to their conjugates so the
    canonical PR #155 sign convention (Im(λ) > 0) holds. The
    complex-symmetric pencil admits both signs of Im(λ) (no enforced
    conjugation), and the eigenvector phase is determined by the
    eigensolver internals; the sign of Im(λ) is a downstream convention
    choice. PR #155 pinned Im(λ) > 0 as canonical."""
    sign_eps = 1.0e-10 * max(float(np.max(np.abs(eigvals.real))), 1.0)
    return np.where(eigvals.imag < -sign_eps, eigvals.conj(), eigvals)


def _q_factor(lam: complex) -> float:
    """Sign-agnostic k-space Q-factor: Q = Re(k) / (2|Im(k)|) with k =
    sqrt(λ). Matches NumPy / Burn canonical formula. Returns +inf for
    purely-real λ (the σ₀=0 PEC regression case)."""
    r = abs(lam)
    re_k = max(0.5 * (r + lam.real), 0.0) ** 0.5
    im_k_mag = max(0.5 * (r - lam.real), 0.0) ** 0.5
    if im_k_mag > 1e-12:
        return float(re_k / (2.0 * im_k_mag))
    return float("inf")


