                f"NumPy J.2 canonical (PR #179) `physical_eigenvalues_complex`: "
                f"physical[0] |Δ| = {d0:.3e}; per-position max |Δ| = "
                f"{max_abs:.3e} over {n_compare} entries (dense LAPACK ZGGEV "
