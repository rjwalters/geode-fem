"""Generate `reference/fixtures/sphere_mie_small/tfjava_baseline.json`.

Phase J.5 / Issue #174 / Epic #88.

This script produces the schema-v1 ``tfjava_baseline.json`` fixture that the
Rust validation test ``crates/geode-validation/tests/sphere_mie_tfjava_reference.rs``
loads. It mirrors the TF-Java JVM + Python eigensolve seam exactly, following
the Phase H.4 precedent (``reference/tf_java/sphere_pml/gen_tfjava_baseline.py``):

1. Mesh I/O, edge enumeration, PEC mask, per-tet centroid vectors, and the
   diagonal anisotropic UPML tensor profile — all matches
   ``reference/numpy/sphere_mie.py``, which in turn mirrors
   ``reference/tf_java/sphere_mie/.../SphereMesh.java`` line-for-line. The
   TF-Java JVM path uses the same lexicographic edge ordering (TreeMap) as
   the NumPy reference (np.unique), so the assembled matrices are
   bit-equivalent modulo floating-point accumulation order.
2. Tensor-ε Nédélec assembly (real K + complex M) via the NumPy reference.
   The TF-Java JVM equivalent (ComplexNedelecAssemblyGraph) contracts the
   per-axis cofactor gram against the [nTets, 3] tensor placeholders inside
   the static graph and emits Re(M) and Im(M) as parallel f64 tensors via
   scatterNd; the NumPy COO->CSR scatter and the TF-Java scatterNd path
   produce identical numerical results to ~f64 ULP because both are batched
   per-element 6x6 scatters with identical input arithmetic. The Phase G.5
   sphere-PEC TF-Java vs NumPy comparison (PR #137) measured ~1.2e-5
   relative drift on the lowest eigenvalue on the 774-node mesh — the same
   accumulation-order effect, smaller on this 48-node mesh.
3. NO sign canonicalization (deliberate divergence from H.4): the
   anisotropic UPML pencil's physical eigenvalues carry Im(λ) < 0 on the
   small mesh — a property of the pencil, agreed on by LAPACK ZGGEV and
   faer QZ (see the J.2 fixture description). Sort by |Re(λ)| ascending
   (matches Burn's `FaerComplexEigensolver` and the J.2/J.4 baselines).
4. Same fixture schema as ``jax_baseline.json`` (PR #180) minus the
   JAX-specific autodiff-probe fields, for downstream comparator
   uniformity.

Why not Maven here?
===================
The fixture is generated by computing the numerical pipeline result; the
fact that TF-Java would produce the same result is verified by the
``tfjava-cube-cavity`` workflow's ``sphere-mie-tfjava`` job (added in
#174), which actually builds and runs the JVM end-to-end on CI and hard-
gates the live eigenvalues against both the NumPy J.2 baseline and this
checked-in fixture. This script is the maintainer convenience for local
regeneration and the source of truth for the checked-in fixture — exactly
the H.4 pattern.

CI policy
=========
The fixture is checked in. The CI workflow regenerates the live JVM
eigenresult on-demand and compares against the checked-in version (same
pattern as ``sphere_pml/tfjava_baseline.json``). If the JVM path drifts
beyond the documented Phase G.5 tolerance, the workflow fails and the
live eigenresult is uploaded as an artifact for the maintainer to inspect.

Usage
=====

    python3 reference/tf_java/sphere_mie/gen_tfjava_baseline.py
    python3 reference/tf_java/sphere_mie/gen_tfjava_baseline.py --sigma0 5.0
    python3 reference/tf_java/sphere_mie/gen_tfjava_baseline.py \\
        --out reference/fixtures/sphere_mie_small/tfjava_baseline.json
"""

