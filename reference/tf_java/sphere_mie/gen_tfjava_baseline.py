    parser.add_argument(
        "--sigma0", type=float, default=5.0,
        help="UPML absorption strength (default 5.0)",
    )
    parser.add_argument(
        "--n-index", type=float, default=1.5,
        help="Dielectric refractive index (default 1.5)",
    )
    parser.add_argument(
        "--k0-ref", type=float, default=2.0,
        help="Reference wavenumber ω heuristic (default 2.0)",
    )
    parser.add_argument(
        "--n-take", type=int, default=5,
        help="Number of physical eigenvalues to retain (default 5)",
    )
    parser.add_argument(
        "--tol", type=float, default=1.0e-4,
        help="Max allowed |Δ| of this path vs NumPy physical band (default 1e-4)",
    )
    args = parser.parse_args()
    out_path = Path(args.out)

    # Import the NumPy reference pipeline. The TF-Java JVM path matches
    # this line-for-line modulo accumulation order; the actual JVM
    # regeneration happens in CI via the tfjava-cube-cavity workflow
    # (sphere-mie-tfjava job) which builds + runs the Maven module.
    from reference.numpy.sphere_mie import (
        classify_modes_against_catalogue,
        load_mie_roots_catalogue,
        q_factor_from_lambda,
        run_sphere_mie,
    )

    print(f"Running NumPy tensor-ε UPML Mie pipeline "
          f"(σ₀={args.sigma0}, n_index={args.n_index}, k0_ref={args.k0_ref})...")
    print("(TF-Java JVM path is numerically equivalent modulo scatterNd accumulation order;")
    print(" actual JVM regeneration runs in CI via the tfjava-cube-cavity workflow.)")

    result = run_sphere_mie(
        args.mesh,
        sigma_0=args.sigma0,
        n_index=args.n_index,
        k0_ref=args.k0_ref,
        n_take=args.n_take,
    )

    n_nodes = result["n_nodes"]
    n_tets = result["n_tets"]
    eigvals_all = result["eigenvalues_all"]
    physical = result["physical_eigenvalues"]
