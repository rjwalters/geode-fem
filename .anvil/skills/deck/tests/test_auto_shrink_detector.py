class TestSkipPaths:
    """Verify graceful-skip when inputs are missing or deps unavailable."""

    def test_missing_pdf_returns_skipped_result(
        self, tmp_path: Path
    ) -> None:
        # A missing deck.pdf must NOT raise — the detector returns a
        # skipped result that the deck-review command can surface as
        # an info-level lint note.
        deck_md = tmp_path / "deck.md"
        deck_md.write_text("# Slide 1\n", encoding="utf-8")
        result = detect_auto_shrink(
            deck_pdf=tmp_path / "does-not-exist.pdf",
            deck_md=deck_md,
        )
        assert result.skipped
        assert result.reason is not None
        assert "not found" in result.reason

    def test_missing_md_returns_skipped_result(
        self, tmp_path: Path
    ) -> None:
        stub_pdf = tmp_path / "deck.pdf"
        stub_pdf.write_bytes(b"%PDF-stub\n")
        result = detect_auto_shrink(
            deck_pdf=stub_pdf,
            deck_md=tmp_path / "does-not-exist.md",
        )
        assert result.skipped
        assert result.reason is not None

    def test_missing_deps_returns_remediation(
        self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        # Stub the preflight to simulate Pillow/numpy not installed.
        # The detector must return a skipped result carrying the
        # remediation message; it must NOT raise.
        from anvil.lib.render import AUTO_SHRINK_REMEDIATION
        import anvil.lib.render as render_module

        monkeypatch.setattr(
            render_module,
            "check_auto_shrink_deps_available",
            lambda: False,
        )

        deck_md = tmp_path / "deck.md"
        deck_md.write_text("# Slide 1\n", encoding="utf-8")
        stub_pdf = tmp_path / "deck.pdf"
        stub_pdf.write_bytes(b"%PDF-stub\n")
        result = detect_auto_shrink(
            deck_pdf=stub_pdf,
            deck_md=deck_md,
        )
        assert result.skipped
        assert result.reason == AUTO_SHRINK_REMEDIATION


class TestThresholdsOverride:
    """A consumer with a custom theme can override the thresholds dataclass."""

    def test_override_floor_eliminates_finding(
        self, auto_shrink_fixture_root: Path
    ) -> None:
        # F2 normally flags slide 3. Raise the absolute floor above 60%
        # and the finding disappears — proves the threshold is honoured.
        case = auto_shrink_fixture_root / "F2"
