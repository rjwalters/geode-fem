"""Tests for ``anvil.skills.deck.lib.auto_shrink_detector`` (issue #102 / #100b).

The detector is the deterministic companion to ``deck-vision``'s qualitative
``v1 vertical_overflow`` — it catches Marp's silent CSS auto-shrink that
neither ``marp_lint`` (source-side) nor ``deck-vision`` (one API call per
slide) catches on its own.

These tests exercise the detector against synthetic Pillow-drawn PNGs
generated by ``fixtures/auto_shrink/conftest.py``. Synthetic-only is the
documented coverage scope (per the maintainer resolution on #102) — the
four canary decks stay in studio as a manual post-merge sanity check.

Fixtures (built at test-time, NOT committed as binary):

- **F1** — three healthy peer slides. No findings.
- **F2** — two healthy peer slides + one auto-shrunk outlier. Slide 3
  must be flagged with ``rule="auto-shrink-fit-compression"``.
- **F3** — one ``title``-class singleton (zero peers). MUST NOT flag;
  must record the class as skipped with a reason.
- **F4** — three equally-light peers. Ratio 1.0; no findings.
- **F5** — peers at bm 7/9/11% (median 9%), candidate at bm 13.5% — the
  ratio condition (1.5x) is matched but the absolute floor (>18%) is
  NOT. Asserts both conditions are required, not OR-of-conditions.
- **F6** — pure classification test against the committed ``deck.md``
  fixture. Asserts ``_classify_slides`` returns the expected per-slide
  class list (mix of ``title``, ``content``, ``ask``).

Deck-distinct filename per the #58 packaging convention.

Runs under either ``python -m unittest discover anvil/skills/deck/tests/``
or ``pytest anvil/skills/deck/tests/``.
"""

