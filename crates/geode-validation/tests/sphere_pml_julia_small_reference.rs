#[test]
fn julia_pml_small_sigma_five_agrees_with_numpy_at_full_scope() {
    // **Issue #160 acceptance criterion — strict scope [0..4]**.
    //
    // The Julia small-mesh dense `LinearAlgebra.eigen(K, M)` path and
    // the NumPy `scipy.linalg.eigvals` path both call LAPACK ZGGEV on
    // the identical 214×214 complex-symmetric pencil; the agreement is
    // expected at LAPACK-vs-LAPACK roundoff (~1e-13 measured) with
    // headroom for OpenBLAS reduction-order variance across the Julia
    // / Python toolchain split.
    //
    // This test cross-checks **all 5 physical modes** (l=1 triplet
    // [0..2] at λ ≈ 1.92 + 0.06j + 2 of l=2 quintuplet [3,4] at
    // λ ≈ 3.28 + 0.15j and 3.57 + 0.12j) at **5e-3 absolute on `|Δ|`**.
    //
    // 5e-3 is conservative — measured residual is 1.3e-13. The wider
    // gate accommodates downstream Julia stdlib upgrades and OpenBLAS
    // build-variant differences without flapping. The actual fixture
    // tolerance on `physical_eigenvalues_complex` is also 5e-3 (set
    // in the generator).
    //
    // This is the **resolution of PR #153 cycle 3's Arpack
    // l=2-unreachability finding** (issue #160): the full-mesh Arpack
    // shift-invert at σ = 1.18 + 0.21j saturates within the l=1 cluster
    // and never reaches l=2 even at nev = 105. The small-mesh dense
    // path surfaces both bands cleanly with no shift-invert
    // basin-of-attraction concerns. The full-mesh
    // `sphere_pml_julia_reference.rs` test (currently scope [0..2])
    // stays as-is — extending its scope is out of scope for #160 and
    // would require either Option (a) multi-shift Arpack or a follow-up
    // issue.
    let julia_fixture = Fixture::load_from(&julia_fixture_path(), FixtureFormat::Json)
        .expect("julia_small_baseline.json should load");
    let numpy_fixture = Fixture::load_from(&numpy_small_fixture_path(), FixtureFormat::Json)
        .expect("NumPy small-mesh baseline.json should load");

    let julia_phys = julia_fixture
        .output_c128("physical_eigenvalues_complex")
        .expect("Julia physical_eigenvalues_complex decodes");
    let numpy_phys = numpy_fixture
        .output_c128("physical_eigenvalues_complex")
        .expect("NumPy physical_eigenvalues_complex decodes");

    assert_eq!(
        julia_phys.data.len(),
        5,
        "Julia must declare 5 physical modes (l=1 triplet + 2 of l=2 quintuplet)"
    );
    assert_eq!(
        numpy_phys.data.len(),
        5,
        "NumPy must declare 5 physical modes"
    );

    let abs_tol: f64 = 5.0e-3;
    let mut max_abs: f64 = 0.0;
    let mut max_idx: usize = 0;

    // **Strict scope [0..4]** — the full set of 5 physical modes.
    for (i, (lj, ln)) in julia_phys
        .data
        .iter()
        .zip(numpy_phys.data.iter())
        .enumerate()
    {
        let delta = (lj - ln).norm();
        if delta > max_abs {
            max_abs = delta;
            max_idx = i;
        }
        assert!(
            delta <= abs_tol,
            "small-mesh physical[{i}] cross-IR drift: Julia={lj:?}, NumPy={ln:?}, \
             |Δ| = {delta:.3e} > {abs_tol:.0e}. Both backends use LAPACK ZGGEV \
             on the identical pencil; a large residual here means either the \
             Julia path stopped using dense LinearAlgebra.eigen, the NumPy \
             fixture was regenerated against a different mesh, or there's a \
             real LAPACK ABI drift to investigate."
        );
    }

    eprintln!(
        "Julia vs NumPy small-mesh sphere-PML cross-IR check passed on \
         **scope [0..4]** (all 5 physical modes — l=1 triplet + 2 of l=2 \
         quintuplet): max |Δ| = {max_abs:.3e} at position [{max_idx}], \
         tolerance = {abs_tol:.0e}. This is the issue #160 Option (b) \
         resolution of PR #153 cycle 3's Arpack l=2-unreachability finding."
    );
}

