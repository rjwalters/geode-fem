#[test]
fn julia_pml_small_sigma_zero_matches_numpy_pec_anchor() {
    // The σ₀=0 PEC anchor (`sigma_zero_lowest_physical_re`) must match
    // the canonical NumPy small-mesh baseline. Both backends use LAPACK
    // on the same real-symmetric pencil at σ₀=0; the agreement is
    // expected at LAPACK-vs-LAPACK roundoff (~1e-13 measured).
    //
    // Tolerance is 5e-5 — matches the NumPy fixture's own
    // `sigma_zero_lowest_physical_re` tolerance_abs (the PR #164 floor
    // for faer-vs-LAPACK on the small mesh; Julia-vs-LAPACK is much
    // tighter but uses the same defensible bound).
    let julia_fixture = Fixture::load_from(&julia_fixture_path(), FixtureFormat::Json)
        .expect("julia_small_baseline.json should load");
    let numpy_fixture = Fixture::load_from(&numpy_small_fixture_path(), FixtureFormat::Json)
        .expect("NumPy small-mesh baseline.json should load");

    let julia_pec_re = julia_fixture
        .output_f64("sigma_zero_lowest_physical_re")
        .unwrap()
        .data[0];
    let numpy_pec_re = numpy_fixture
        .output_f64("sigma_zero_lowest_physical_re")
        .unwrap()
        .data[0];

    let delta = (julia_pec_re - numpy_pec_re).abs();
    assert!(
        delta < 5e-5,
        "σ₀=0 PEC anchor mismatch: Julia={julia_pec_re}, NumPy={numpy_pec_re}, |Δ|={delta:.3e} > 5e-5"
    );
}

#[test]
fn julia_pml_small_sigma_five_has_pml_absorption_signature() {
    // Physical-content correctness: with σ₀ = 5, all lowest physical
    // modes must have Im(λ) ≥ 0 under the Wave-2 canonical sign
    // convention (PR #155 Judge's binding decision).
    let fixture = Fixture::load_from(&julia_fixture_path(), FixtureFormat::Json)
        .expect("julia_small_baseline.json should load");

    let phys = fixture
        .output_c128("physical_eigenvalues_complex")
        .expect("c128 output decodes");
    let q = fixture.output_f64("q_factor_lowest_physical").unwrap().data[0];

    for (i, lam) in phys.data.iter().enumerate() {
        assert!(
            lam.im >= -1e-6,
            "physical[{i}] = {lam:?} has Im(λ) < 0 — sign-convention regression \
             (Wave-2 canonical: Im(λ) > 0 per PR #155)"
        );
    }

    // Q-factor must be finite and positive. On the small mesh's coarse
    // discretization the ground mode is high-Q (~34.8).
    assert!(q.is_finite(), "Q-factor must be finite, got {q}");
    assert!(
        q > 0.0,
        "Q-factor for the lowest physical mode must be positive (PML absorption), got {q}"
    );

    // Re(λ) for the canonical small-mesh physical[0] is ≈ 1.92 (vs
    // the full mesh's ≈ 1.18). Loose sanity bound [1.5, 2.5].
    let re_lowest = phys.data[0].re;
    assert!(
        (1.5..2.5).contains(&re_lowest),
        "Re(λ_lowest_physical) = {re_lowest} outside [1.5, 2.5] sanity band \
         for σ₀=5 small-mesh PML — expected canonical band around 1.92"
    );
}

