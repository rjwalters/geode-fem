//! Cross-IR sphere-PML loader/comparator against the Julia **small-mesh**
//! baseline (issue #160 — Option (b) resolution).
//!
//! Loads `reference/fixtures/sphere_pml/julia_small_baseline.json` (the Julia
//! reference for the vector-Nédélec sphere-PML eigenmode pipeline on the
//! 197-tet small-mesh fixture introduced in PR #164 / issue #158) and asserts:
//!
//! 1. **Schema conformance**: canonical v1 schema with the small-mesh-specific
//!    output field set (`physical_eigenvalues_complex`, `sigma_zero_lowest_physical_re`).
//! 2. **c128 round-trip**: `Fixture::output_c128` decodes the real-imag
//!    interleaved on-disk payload into `Vec<Complex64>`.
//! 3. **σ₀ = 0 PEC anchor**: `sigma_zero_lowest_physical_re` matches the
//!    canonical NumPy small-mesh baseline (PR #164) at 5e-5 absolute (the
//!    small mesh has no separate PEC baseline; the in-fixture anchor is
//!    the PEC regression).
//! 4. **σ₀ = 5 PML sign convention**: `physical_eigenvalues_complex` has
//!    `Im(λ) ≥ 0` per the Wave-2 canonical sign convention (PR #155).
//! 5. **Cross-IR scope [0..4] vs NumPy small-mesh baseline**: the Julia
//!    `physical_eigenvalues_complex` field agrees with the NumPy
//!    `physical_eigenvalues_complex` at **5e-3 absolute on `|Δ|`** for all 5
//!    modes — covering the **l=1 triplet** (positions [0..2]) and **2 of
//!    the l=2 quintuplet** (positions [3,4]). This is the resolution of
//!    PR #153 cycle 3's Arpack l=2-unreachability finding: dense Julia
//!    eigen on the small mesh surfaces both bands cleanly.
//!
//! # Why this test exists
//!
//! The companion `sphere_pml_julia_reference.rs` test (full mesh, 3300-DOF
//! pencil) restricts its strict cross-IR scope to positions [0..2] because
//! the full-mesh path uses Arpack shift-invert at σ = 1.18 + 0.21j, which
//! saturates within the l=1 lossy cluster and never reaches the l=2
//! quintuplet (canonical NumPy positions [3,4]). This small-mesh sibling
//! resolves issue #160 by switching to dense `LinearAlgebra.eigen(K, M)`
//! on the 214-DOF interior pencil — see
//! `reference/julia/sphere_pml_small.jl` for the design rationale.
//!
//! Both backends (Julia and NumPy) here use LAPACK ZGGEV on the identical
//! complex-symmetric pencil, so agreement is expected at LAPACK-vs-LAPACK
//! roundoff (~1e-13 measured); 5e-3 is the conservative floor accommodating
//! Julia OpenBLAS vs scipy OpenBLAS reduction-order variance across the
//! Julia / Python toolchain split.
//!
//! # Running
//!
//! ```sh
//! cargo test -p geode-validation --test sphere_pml_julia_small_reference
//! ```

use std::collections::BTreeMap;
use std::path::PathBuf;

use geode_validation::diff::FieldStatus;
use geode_validation::{Complex64, Fixture, FixtureFormat};

/// Walk up from `CARGO_MANIFEST_DIR` to find the repo root.
fn repo_root() -> PathBuf {
    let manifest = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    for ancestor in manifest.ancestors() {
        if ancestor.join("reference").is_dir() {
            return ancestor.to_path_buf();
        }
    }
    panic!(
        "could not find `reference/` directory walking up from {}",
        manifest.display()
    );
}

/// The Julia small-mesh baseline, generated by
/// `reference/julia/gen_sphere_pml_small_baseline.jl`.
fn julia_fixture_path() -> PathBuf {
    repo_root().join("reference/fixtures/sphere_pml/julia_small_baseline.json")
}

/// The NumPy small-mesh canonical baseline (PR #164, issue #158).
fn numpy_small_fixture_path() -> PathBuf {
    repo_root().join("reference/fixtures/sphere_pml_small/baseline.json")
}

