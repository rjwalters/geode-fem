#[test]
fn julia_pml_small_fixture_loads_with_canonical_schema() {
    let fixture = Fixture::load_from(&julia_fixture_path(), FixtureFormat::Json)
        .expect("julia_small_baseline.json should load");
    assert_eq!(
        fixture.fixture_id,
        "sphere_pml_small/n48_pml_eigenmode_julia"
    );
    assert_eq!(fixture.schema_version, "1");

    // Required input fields.
    for key in [
        "mesh_path",
        "sigma_0",
        "r_sphere",
        "r_pml_inner",
        "r_buffer",
        "n_index",
        "epsilon_r_complex",
    ] {
        assert!(
            fixture.inputs.contains_key(key),
            "Julia small-mesh PML fixture missing required input `{key}`"
        );
    }
    assert_eq!(
        fixture.inputs["epsilon_r_complex"].dtype, "c128",
        "epsilon_r_complex must be c128 (the input-side complex contract)"
    );

    // Required output fields — matches the NumPy small-mesh schema.
    for key in [
        "n_nodes",
        "n_tets",
        "n_edges",
        "n_interior_edges",
        "spurious_dim",
        "n_spurious_observed",
        "k_int_frobenius_complex",
        "m_int_frobenius_complex",
        "eigenvalues_lowest_complex",
        "physical_eigenvalues_complex",
        "q_factor_lowest_physical",
        "sigma_zero_lowest_physical_re",
    ] {
        assert!(
            fixture.outputs.contains_key(key),
            "Julia small-mesh PML fixture missing required output `{key}`"
        );
    }
    assert_eq!(
        fixture.outputs["eigenvalues_lowest_complex"].dtype, "c128",
        "eigenvalues_lowest_complex must be c128"
    );
    assert_eq!(
        fixture.outputs["physical_eigenvalues_complex"].dtype, "c128",
        "physical_eigenvalues_complex must be c128"
    );
    assert_eq!(
        fixture.outputs["q_factor_lowest_physical"].dtype, "f64",
        "q_factor_lowest_physical is a real-valued derived metric"
    );
    assert_eq!(
        fixture.outputs["sigma_zero_lowest_physical_re"].dtype, "f64",
        "sigma_zero_lowest_physical_re is the PEC anchor"
    );
}

#[test]
